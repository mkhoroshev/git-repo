# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

import os, random, re
result = []

path = os.path.join('files','number.txt')
with open (path, 'w') as file:
    file.write(str(random.randint(1,9)))
    file.write(''.join([str(random.randint(0,9)) for _ in range(2499)]))

with open (path, 'r') as file:
    number = file.read()
    for i in range(0, 9):
        result.append(len(re.findall(str(i), number, re.MULTILINE)))

big = max(result)
print(f'Самая длинная последовательность чисел: {result.index(big)} - {big}')

